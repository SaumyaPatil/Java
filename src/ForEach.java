class ForEach{
    public static void main(String[] args) {
        int a[] = {10, 20, 30, 40};

        //for each loop in case of arrays is better than for loop as you dont have to give condition nor you have to make initialisation
        for (int b:a){   //b variable assigns first value of a array variable
            System.out.println(b);
        }

    }
}
