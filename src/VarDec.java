class VarDec{
    public static void main(String[] args) {

        //Variable is name of memory loc where you store data.
        //You can store data in the loc and also during runtime.

        int a=10;
        System.out.println(a);

        int _b=20;
        System.out.println(_b);

        int $c = 30;
        System.out.println($c);

        int a2=40;
        System.out.println(a2);

        //int 2a;   //This is wrong. Variable name should either start from a letter, underscore or a dollar symbol.

        //int 10=a; //Value should be in the left of operator

        int su_m=50;
        System.out.println(su_m);

        //int su m;  //not allowed
        int su2m;  //allowed

        int int=190; //this is not allowed as it is a keyword or a reserved word for data type in java

        //data_type var_name=10;  //syntax to declare variable
    }
}
